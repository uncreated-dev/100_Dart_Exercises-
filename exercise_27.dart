// Write a Dart program that tells a short story. 

void main() {
  String character = 'Chinedu';
  String place = 'the busy streets of Lagos';
  String adventure = 'raced through traffic to catch the last bus';
  String friend = 'Ngozi, his childhood friend';
  
  // Printing the Nigerian feel story
  print('$character woke up early to hustle in $place on a hot afternoon.');
  print('He needed to make it to his office but got caught in a jam.');
  print('In the middle of it all, he $adventure, with honking cars all around him.');
  print('Just then, he bumped into $friend, who was also running late.');
  print('Together, they laughed, complained about the crazy traffic, and made it just in time.');
}