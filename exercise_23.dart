// Write a Dart program that uses a primitive and non primitive data types
// and print their values to the console.

 void main() {
  // Primitive Data Types
  int age = 25; // Integer
  double height = 5.9; // Decimal number
  bool isStudent = true; // True or False
  String name = 'Alice'; // Text

  // Non-Primitive Data Types
  List<String> fruits = ['Apple', 'Banana', 'Cherry']; // List of items
  Map<String, int> studentScores = {'Math': 90, 'Science': 85}; // Key-Value pairs

  // Printing values
  print('Name: $name');
  print('Age: $age');
  print('Height: $height');
  print('Is Student: $isStudent');
  print('Fruits: $fruits');
  print('Student Scores: $studentScores');
}