// Write a Dart program that defines several variables of different data types 
// and using one print statement output all values. 


void main() {
  
  // Defining variables of different data types
  int age = 25;
  double height = 5.9;
  bool isStudent = true;
  String name = 'Alice';
  List<String> fruits = ['Apple', 'Banana', 'Cherry'];
  Map<String, int> studentScores = {'Math': 90, 'Science': 85};

  // Printing all values in one statement
  print('Name: $name, Age: $age, Height: $height, Is Student: $isStudent, Fruits: $fruits, Student Scores: $studentScores');
}